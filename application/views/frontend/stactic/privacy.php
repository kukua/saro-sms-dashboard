Kukua B V wants to assure you that it respects your privacy and the confidentiality of your personal information when using the Kukua B V website. Kukua B V follows the principles outlined below, which are in accordance with practices for customer privacy and data protection as required under California and Federal Law.

1. Personally Identifiable Information

Personally Identifiable Information includes information such as: your first or last name; your home address; your e-mail address; your telephone number; your social security number; or any other identifier that permits the physical or online contacting of a specific individual.

1a. Personally Identifiable Information – Visiting or Browsing Web Site Only

Kukua B V does not automatically collect personally identifiable information from visitors who do no more than visit our Web site.

1b. Personally Identifiable Information – Engaging in Activities and Services on Web Site

Kukua B V may collect your Personally Identifiable Information when you engage in activities and services on our Web Site. Such activities and services include: registering for our publications or services; sending comments to Kukua B V through the use of our Customer Service comment form; entering contests; or participating in a survey. These activities are completely optional for you to engage in. However, if you want to engage in these activities certain Personally Identifiable Information must be submitted in order for Kukua B V to deliver the products, information, and services you request efficiently. Kukua B V will not sell or share your Personally Identifiable Information outside of Kukua B V unless it is otherwise conspicuously posted on the relevant activity’s rules and terms.

1c. Personally Identifiable Information – Compass Card

Kukua B V has developed a separate Privacy Policy for Personally Identifiable Information collected from Compass Card transactions. The Compass Card Privacy Policy can be found at http://compass.511sd.com/.

2. Non Personally Identifiable Information Collected Automatically

Kukua B V uses Google Analytics (http://www.google.com/analytics/) to help analyze how visitors use the Kukua B V Web site. Google Analytics uses first-party cookies to collect standard internet logs and visitor behavior information in an anonymous form, such as:

 

The type of browser and operating system used to access our site; 
The date and time you access our site; 
The pages you visit; and 
If you linked to our Web site from another Web site, the address of that Web site.
Kukua B V uses this non-personally identifiable information only to maintain and improve the Web site’s performance. Google Analytics does collect the IP address assigned to you on the date you visit this site; however, this information is not shared with Kukua B V. Google’s ability to use and share information collected about your visits to this site is restricted by the Google Analytics Terms of Service (http://www.google.com/analytics/terms/us.html) and the Google Privacy Policy (http://www.google.com/policies/privacy/). If you disable cookies on your browser, Google Analytics can be prevented from “recognizing” you on return visits to this Web site.

3. Security

Kukua B V monitors network traffic to identify unauthorized attempts to upload, change or otherwise cause damage to the Website. If any unauthorized access to or use of Personally Identifiable Information occurs, despite the protections established within this Privacy Policy, Kukua B V will notify you of a breach in security of the system following discovery in one or more of the following ways: email, mail, posting of the breach on the Kukua B V Website or by notifying the media.

Kukua B V cannot guarantee that incoming email before received by Kukua B V will not be intercepted. Please do not send highly sensitive information over email. Instead, contact us by mail or telephone.

4. Children’s Privacy Policy

The Children’s Online Privacy Protection Act establishes protections of Personally Identifiable Information collected from children under the age of 13. Personally Identifiable Information may be collected when a child engages in activities or services provided on the Web site, such as registers for our publications or services, enters a contest or participates in a survey. Kukua B V will only ask for information that is necessary to participate in such activity. When a child sends comments to Kukua B V through the use of our Customer Service comment form or sends an E-mail to Kukua B V, Kukua B V may use the Personally Identifiable Information, such as an e-mail address, to respond directly on a one-time basis to a specific request from the child.

When Kukua B V has actual knowledge that we have collected Personally Identifiable Information from children engaged in activities or services provided on the Web site, Kukua B V will make all reasonable efforts to obtain parental or legal guardian consent before any further use of the Personally Identifiable Information occurs. When Kukua B V does obtain parental or legal guardian consent, Kukua B V will maintain reasonable procedures to protect the confidentiality, security and integrity of the Personally Identifiable Information collected from children.

When Kukua B V does not obtain parental or legal guardian consent to use Personally Identifiable Information for a child or after Kukua B V uses Personally Identifiable Information to respond directly to a specific request from a child, Kukua B V will no longer maintain the Personally Identifiable Information in any retrievable form.

5. Notice of Changes to this Policy

If Kukua B V needs to change its privacy policy, those changes will be posted here so that you will always know what information Kukua B V gathers and how it might be used.

