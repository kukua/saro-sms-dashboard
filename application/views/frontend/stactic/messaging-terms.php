What if I don’t want to receive any more SMS promotional messages?

To stop receiving only Kukua B V Paint Pro Alerts, simply text STOP to the short code PAINT (72468), or call 1-800-474-3794. After doing so, you will receive confirmation of your opt-out, and thereafter, you will no longer receive any Kukua B V messages.

